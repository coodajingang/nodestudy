export function int2(v: number): number {
  return Math.floor(v);
}

export function mod2(v: number, n: number) {
  return (v % n + n) % n;
}

export function sqrt(x: number) {
  return Math.sqrt(x);
}

export function floor(x: number) {
  return Math.floor(x);
}

export function abs(x: number) {
  return Math.abs(x);
}

export function sin(x: number) {
  return Math.sin(x);
}

export function cos(x: number) {
  return Math.cos(x);
}

export function tan(x: number) {
  return Math.tan(x);
}

export function asin(x: number) {
  return Math.asin(x);
}

export function acos(x: number) {
  return Math.acos(x);
}

export function atan(x: number) {
  return Math.atan(x);
}

export function atan2(y: number, x: number) {
  return Math.atan2(y, x);
}